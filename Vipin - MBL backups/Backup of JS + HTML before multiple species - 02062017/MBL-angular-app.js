angular.module("manxBirdLifeApp")
    .controller("formController", ['$scope', 'species', 'location', 'sightUser', '$http', 'blockUI', '$timeout', 'Upload','$window',
        function ($scope, species, location, sightUser, $http, blockUI, $timeout, Upload, $window) {

            $scope.dateOptions = {
                maxDate: new Date(),
                showWeeks: false,
                datepickerMode:'month'
            };

            $scope.formats = ['dd MMMM yyyy', 'dd-MMMM-yyyy', 'yyyy/MM/dd', 'dd.MM.yyyy', 'shortDate'];
            $scope.format = $scope.formats[0];
            $scope.altInputFormats = ['M!/d!/yyyy'];

            //TODO - move this to webservice
            //$scope.speciesArray = speciesService.getAllSpeciesList;
            //$scope.locationArray = locationService.getAllLocationsList;
            $scope.speciesArray = species.query();
            $scope.locationArray = location.query();

            //new species info
            function speciesInfo() {

            }


            //new sightingInfo fields
            function sightingInfo() {
                this.sightUserID = '',
                this.speciesSelected ='',
                this.locationSelected ='',
                this.gridReference='',
                this.dt= new Date(),
                this.myTime = (new Date()).getDate(),
                this.speciesnumber='',
                this.approximation='',
                this.details='',
                this.dropFile = ''	,
                this.calendarOpened = false,
                this.TabOpen = false,
                this.openCalendar = function () {
                    this.calendarOpened = true;
                },
                this.formatSpeciesLabel = function(model) {
                    var matchFound = false;
                    for (var i=0; i< $scope.speciesArray.length; i++) {
                        if (model === $scope.speciesArray[i].ID) {
                            matchFound = true;
                            return $scope.speciesArray[i].Name;
                        }
                    }
                    if (!matchFound) {
                        return model;
                    }
                },
                this.formatLocationLabel = function(model) {
                    var matchFound = false;
                    for (var i=0; i< $scope.locationArray.length; i++) {
                        if (model === $scope.locationArray[i].ID) {
                            matchFound = true;
                            return $scope.locationArray[i].Name;
                        }
                    }
                    if (!matchFound) {
                        return model;
                    }
                }
            }

            $scope.data = {};
            //array to hold sighting information
            $scope.data.sightingsArray =  [];

            //add one sighting to start with
            $scope.data.sightingsArray.push(new sightingInfo());

            $scope.showAddButton = true;

            //add more sighting
            $scope.addSighting = function() {
                $scope.data.sightingsArray.push(new sightingInfo());
                //allow to add a maximum of 4 sighting
                if ($scope.data.sightingsArray.length >= 4 ) {
                    $scope.showAddButton = false;
                }
            };

            //remove current sighting
            $scope.removeSighting = function(pos) {
                if (pos > -1) {
                    $scope.data.sightingsArray.splice(pos, 1);
                }
            };

            $scope.uploadNow = function() {

                // Block the user interface
                blockUI.start("Saving sight information...");

                sightUser.save(
                    {
                        SightingUserId:0,
                        Name: $scope.data.userName,
                        Email: $scope.data.userEmail,
                        sensitiveRecordCheckbox: $scope.data.chkSensitiveDoNotDisplay,
                        AnonymousCheckbox: $scope.data.chkKeepAnonymous
                    },
                    function(user){
                        blockUI.message('Uploading sight data and images ...');

                        angular.forEach($scope.data.sightingsArray, function(value, key){
                            value.sightUserID = user.SightingUserId;
                            //upload all sightings info to web api
                            Upload.upload({
                                url: "http://46.32.253.178/api/sightings", // web api url
                                method: "POST",
                                data: { fileUploadObj: JSON.stringify(value) },
                                file: value.dropFile
                            }).progress(function (evt) {
                                // get upload percentage
                                //console.log('percent: ' + parseInt(100.0 * evt.loaded / evt.total));
                            }).success(function (data, status, headers, config) {
                                // file is uploaded successfully
                                console.log(data);
                            }).error(function (data, status, headers, config) {
                                // file failed to upload
                                console.log(data);
                            });
                        });

                        $scope.data.submittedOK = true;
                    },
                    function(error) {
                        console.log(error.message);
                    }
                );

                blockUI.done(function() {

                });

                blockUI.stop();
            };


        }]);

